    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        try {
            FrameCadastroRpc telaCadastro = new FrameCadastroRpc(serviceRpc);
            FramePrincipal.adicionaTela(telaCadastro, false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        try {
            if (jList.getSelectedValue() == null) {
                JOptionPane.showMessageDialog(null, "Selecione um rpc!");
            }
            FrameCadastroRpc telaCadastro = new FrameCadastroRpc(serviceRpc);
            telaCadastro.setRpc((Rpc) jList.getSelectedValue());
            FramePrincipal.adicionaTela(telaCadastro, false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            if (jList.getSelectedValue() == null) {
                JOptionPane.showMessageDialog(null, "Selecione um rpc!");
            } else {
                serviceRpc.deletar((Rpc) jList.getSelectedValue());
                AtualizarLista();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonExcluirActionPerformed

